<div class="col-xs-12 col-sm-4 col-md-4"> 
<h3 class="main-circle-header " >Полы наливные</h3>  
  <p class="article-p ">Наиболее практичными и долговечными финишными покрытиями пола являются паркет или же массивная доска, а также ламинат. Востребованы и полы наливные, которые отличает совмещение функций стяжки и декоративного слоя.</p>
