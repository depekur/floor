  <h3 class="main-circle-header " >Настил полов</h3>
  <p class="article-p ">К одному из логичных и необходимых этапов ремонта относят настил полов. В зависимости от предназначения помещений выбирается финишное покрытие. Например, полы на кухне могут быть выложены плиткой или ламинатом, а полы в квартире паркетной доской. </p>
