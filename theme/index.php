<h3 class="main-circle-header " >Теплый пол. Удобство и экономичность.</h3>  
  <p class="article-p ">В отапливаемых помещениях, где система отопления выполнена по традиционной технологии, полы являются самым холодным местом, и что бы дома или в офисе был теплый пол, необходимо уйти от стереотипов. Привычные для нас нагревательные элементы, будь то инфракрасный рефлектор, тепловая пушка, радиатор или конвектор, нагревают воздух в помещении, и тепло в таком случае распространяется не равномерно, чем выше – тем теплее.</p>
