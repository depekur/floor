      <p class="main-about-p">Таким образом, в сравнение с обычным деревянным полом регулируемый стоит немного дороже (примерно на 15 - 20%). Но он не скрипит, не выходит из строя при различных перепадах влажности, в том числе и при протечках, и позволяет стелить на него паркетные и ламинатные покрытия, требующие высокой точности выравнивания.</p>
