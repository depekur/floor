  <h3 class="main-circle-header " >Устройство полов. Устройство теплого пола</h3>
  <p class="article-p ">Устройство полов – это процесс довольно сложный и кропотливый, требующий определенных знаний и навыков. Когда речь заходит о таком понятие, как устройство теплого пола, принято относить его к технологии монтажа регулируемого пола.</p>
</div>
<div class="col-xs-12 col-sm-4 col-md-4"> 
<h3 class="main-circle-header " >Паркетные полы</h3>  
  <p class="article-p ">Когда идет планировка квартирного помещения, где ремонт полов или выравнивание полов играет важную роль, всегда хочется обеспечить каждой комнате тепло, уют и красоту. При этом хочется, чтобы полы в квартире были еще и практичные. Паркетные полы способны выдержать любые требования заказчика. В чем преимущества паркетных полов?</p>
</div>
<div class="col-xs-12 col-sm-4 col-md-4"> 
  <h3 class="main-circle-header " >Деревянные полы</h3>
  <p class="article-p ">Деревянный пол – это не только способ выделиться и заявить о своем исключительном вкусе и стабильном достатке, но и проявить истовые хозяйские качества. Ведь полы из дерева это не только роскошь, но и явная особенность для любой квартиры, дома или дачи. Что же уникального в деревянных полах?</p>
