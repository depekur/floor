      <p class="main-about-p">Лаги в обычной деревянной конструкции укладываются на бетонное или другое основание без жёсткого крепления к нему, а выравниваются подкладыванием щепочек, кирпичиков, песка. Со временем такая лага быстро расшатывается, и пол приходит в негодность. В конструкциях регулируемого пола лага  жёстко закреплена к основанию, не разболтается со временем и не рассохнется даже при намокании в аварийных случаях (при протечках), как обычная.</p>
